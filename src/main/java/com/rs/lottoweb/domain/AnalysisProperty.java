package com.rs.lottoweb.domain;

public class AnalysisProperty {

	protected int testCount;
	protected int analysisCount;
	protected int minRange;
	protected int maxRange;
	protected int rangeIncrease;
	protected int minSeq;
	protected int maxSeq;
	public int getTestCount() {
		return testCount;
	}
	public void setTestCount(int testCount) {
		this.testCount = testCount;
	}
	public int getAnalysisCount() {
		return analysisCount;
	}
	public void setAnalysisCount(int analysisCount) {
		this.analysisCount = analysisCount;
	}
	public int getMinRange() {
		return minRange;
	}
	public void setMinRange(int minRange) {
		this.minRange = minRange;
	}
	public int getMaxRange() {
		return maxRange;
	}
	public void setMaxRange(int maxRange) {
		this.maxRange = maxRange;
	}
	public int getRangeIncrease() {
		return rangeIncrease;
	}
	public void setRangeIncrease(int rangeIncrease) {
		this.rangeIncrease = rangeIncrease;
	}
	public int getMinSeq() {
		return minSeq;
	}
	public void setMinSeq(int minSeq) {
		this.minSeq = minSeq;
	}
	public int getMaxSeq() {
		return maxSeq;
	}
	public void setMaxSeq(int maxSeq) {
		this.maxSeq = maxSeq;
	}
}
